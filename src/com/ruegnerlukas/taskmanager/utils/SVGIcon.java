package com.ruegnerlukas.taskmanager.utils;

public class SVGIcon {


	public final String data;
	public final int width;
	public final int height;




	/**
	 * @param data   the svg-data of this icon
	 * @param width  the original width of this icon in pixels
	 * @param height the original height of this icon in pixels
	 */
	public SVGIcon(String data, int width, int height) {
		this.data = data;
		this.width = width;
		this.height = height;
	}


}
